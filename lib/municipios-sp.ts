export function nomeMunicipio(codigo: string | null | undefined): string {
  if (!codigo) return "Não informado";
  const cod = String(codigo).trim().replace(/\D/g, "");
  return MUNICIPIOS_SP[cod] ?? MUNICIPIOS_SP[cod.padStart(6, "0")] ?? cod;
}

/** Retorna true se o código está mapeado para um nome */
export function temNome(codigo: string | null | undefined): boolean {
  if (!codigo) return false;
  const cod = String(codigo).trim().replace(/\D/g, "");
  return cod in MUNICIPIOS_SP || cod.padStart(6, "0") in MUNICIPIOS_SP;
}
